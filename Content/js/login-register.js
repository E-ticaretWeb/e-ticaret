let currentAnimation = null;

// Email Input Animation
document.querySelector('#email').addEventListener('focus', function () {
    if (currentAnimation) currentAnimation.pause();
    currentAnimation = anime({
        targets: 'path',
        strokeDashoffset: {
            value: 0,
            duration: 700,
            easing: 'easeOutQuart'
        },
        strokeDasharray: {
            value: '240 1386',
            duration: 700,
            easing: 'easeOutQuart'
        }
    });
});

